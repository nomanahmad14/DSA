public class Switch {
    public static void main(String[] args) {
        int day=5;

        switch (day){
            case 1:
            System.out.println("DAY IS PEER");
            break;

            case 2:
            System.out.println("DAY IS mangal");
            break;

            case 3:
            System.out.println("DAY IS budh");
            break;

            case 4:
            System.out.println("DAY IS jumeraat");
            break;

            case 5:
            System.out.println("DAY IS jumah");
            break;

            case 6:
            System.out.println("DAY IS hafta");
            break;

            case 7:
            System.out.println("DAY IS itwaar");
            break;
        }
    }
}
